#include "Tile.h"

//-------------------------------------------------------------------------------
Tile::Tile()
:	m_occupied(false),
	m_x(0),
	m_y(0)
{
}

//-------------------------------------------------------------------------------
Tile::~Tile()
{
}

//-------------------------------------------------------------------------------
bool Tile::IsOccupied() const
{
	return m_occupied;
}

//-------------------------------------------------------------------------------
void Tile::SetOccupied(bool occupied)
{
	m_occupied = occupied;
}

//-------------------------------------------------------------------------------
void Tile::SetIndex(int x, int y)
{
	m_x = x;
	m_y = y;
}

//-------------------------------------------------------------------------------
int Tile::GetX() const
{
	return m_x;
}

//-------------------------------------------------------------------------------
int Tile::GetY() const
{
	return m_y;
}
